#include "libft.h"

void	matrix_cpy(double src[4][4], double cpy[4][4])
{
	cpy[0][0] = src[0][0];
	cpy[0][1] = src[0][1];
	cpy[0][2] = src[0][2];
	cpy[0][3] = src[0][3];
	cpy[1][0] = src[1][0];
	cpy[1][1] = src[1][1];
	cpy[1][2] = src[1][2];
	cpy[1][3] = src[1][3];
	cpy[2][0] = src[2][0];
	cpy[2][1] = src[2][1];
	cpy[2][2] = src[2][2];
	cpy[2][3] = src[2][3];
	cpy[3][0] = src[3][0];
	cpy[3][1] = src[3][1];
	cpy[3][2] = src[3][2];
	cpy[3][3] = src[3][3];
}
