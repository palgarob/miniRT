#include "libft.h"

t_vec	vec_cross(t_vec a, t_vec b)
{
	return (vec(
			a.y * b.z - a.z * b.y,
			a.z * b.x - a.x * b.z,
			a.x * b.y - a.y * b.x
		));
}
